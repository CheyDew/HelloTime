public class Time {
    public static void main(String[] args) {
      int time = 12;
      if (time < 11) {
        System.out.println(" Hi ! Good morning.");
      } else if (time < 18) {
        System.out.println("Hi ! Good evening.");
      }  else {
        System.out.println("Hi ! Good night.");
      }
    }
  }